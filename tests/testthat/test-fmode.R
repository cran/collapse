Mode <- function(x, na.rm = FALSE) {
  if(na.rm) x <- x[!is.na(x)]
  ux <- unique(x)
  ux[which.max(tabulate(match(x, ux)))]
}

weighted.Mode <- function(x, w, na.rm = FALSE) {
  ax <- attributes(x)
  if(na.rm) {
    cc <- complete.cases(x, w)
    w <- w[cc]
    x <- x[cc]
  }
  sw <- fsum.default(w, x)
  max <- which.max(sw)
  res <- names(sw)[max]
  storage.mode(res) <- storage.mode(x)
  `attributes<-`(res, ax)
}

